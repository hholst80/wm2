    def _load_restart_state(self, state: dict):
        """Apply non-window state from a restart state dict."""
        self.current_desktop_id = state.get("current_desktop_id", 1)
        self.floating_active = state.get("floating_active", False)
        self.floating_has_focus = state.get("floating_has_focus", self.floating_active)
        if not self.floating_active:
            self.floating_has_focus = False
        for did_str, dstate in state.get("desktops", {}).items():
            did = int(did_str)
            desktop = self.desktops.get(did)
            if desktop is None:
                continue
            layout_val = dstate.get("layout")
            if layout_val:
                try:
                    desktop.layout = LayoutMode(layout_val)
                except ValueError:
                    pass
            desktop.focused_index = dstate.get("focused_index", 0)
            side_val = dstate.get("focused_side")
            if side_val:
                try:
                    desktop.focused_side = Side(side_val)
                except ValueError:
                    pass
        self._saved_state = state
        self._saved_state_idle_cycles = 0
        self._saved_match_index = None
        logger.info("Applied restart state (desktop=%d, floating=%s)",
                     self.current_desktop_id, self.floating_active)

    def _match_saved_window(self, win: WindowState) -> Optional[dict]:
        """Match a window against saved state entries; returns entry or None."""
        if self._saved_state is None:
            return None
        # Lazily build match index on first call
        if self._saved_match_index is None:
            idx = defaultdict(list)
            # Index desktop windows
            for did_str, dstate in self._saved_state.get("desktops", {}).items():
                did = int(did_str)
                for entry in dstate.get("windows", []):
                    e = dict(entry, desktop_id=did, source="windows")
                    idx[(entry.get("app_id"), entry.get("title"))].append(e)
                for entry in dstate.get("left_stack", []):
                    e = dict(entry, desktop_id=did, source="left_stack")
                    idx[(entry.get("app_id"), entry.get("title"))].append(e)
                for entry in dstate.get("right_stack", []):
                    e = dict(entry, desktop_id=did, source="right_stack")
                    idx[(entry.get("app_id"), entry.get("title"))].append(e)
            # Index floating windows
            for entry in self._saved_state.get("floating_stack", []):
                e = dict(entry, desktop_id=0, source="floating")
                idx[(entry.get("app_id"), entry.get("title"))].append(e)
            # Index popup windows
            for entry in self._saved_state.get("popup_stack", []):
                e = dict(entry, desktop_id=-1, source="popup")
                idx[(entry.get("app_id"), entry.get("title"))].append(e)
            self._saved_match_index = idx
        key = (win.app_id, win.title)
        entries = self._saved_match_index.get(key)
        if entries:
            return entries.pop(0)
        return None

    def _restore_window_from_saved(self, win: WindowState, saved: dict):
        """Place a window according to saved state entry."""
        did = saved["desktop_id"]
        source = saved["source"]
        if did == -1 or source == "popup":
            # Popup/dialog window
            win.desktop_id = -1
            self.popup_stack.append(win)
            win.pos_x = saved.get("pos_x", 0)
            win.pos_y = saved.get("pos_y", 0)
            win.proxy.set_tiled(EDGE_NONE)
            logger.info("Restored popup window %s/%s at (%d,%d)",
                        win.app_id, win.title, win.pos_x, win.pos_y)
        elif did == 0 or source == "floating":
            # Floating window
            win.desktop_id = 0
            self.floating_stack.append(win)
            win.pos_x = saved.get("pos_x", 0)
            win.pos_y = saved.get("pos_y", 0)
            logger.info("Restored floating window %s/%s at (%d,%d)",
                        win.app_id, win.title, win.pos_x, win.pos_y)
        else:
            desktop = self.desktops.get(did)
            if desktop is None:
                desktop = self.desktops[1]
                did = 1
            win.desktop_id = did
            if source == "left_stack":
                win.side = Side.LEFT
                desktop.left_stack.append(win)
                logger.info("Restored window %s/%s on desktop %d left_stack",
                            win.app_id, win.title, did)
            elif source == "right_stack":
                win.side = Side.RIGHT
                desktop.right_stack.append(win)
                logger.info("Restored window %s/%s on desktop %d right_stack",
                            win.app_id, win.title, did)
            else:
                # MAX or FULLSCREEN layout — use windows list
                desktop.windows.append(win)
                logger.info("Restored window %s/%s on desktop %d windows",
                            win.app_id, win.title, did)

    # -------------------------------------------------------------------
    # Window placement
    # -------------------------------------------------------------------
    def _place_new_window(self, win: WindowState):
        """Place a newly created window on the appropriate desktop/stack."""
        output = self.primary_output
        if output is None:
            return

        # Check for saved restart state match
        saved = self._match_saved_window(win)
        if saved is not None:
            self._restore_window_from_saved(win, saved)
            if win.desktop_id == 0:
                self._propose_window_dimensions(win)
            return

        # Popup/dialog detection: windows with a living parent
        if win.parent is not None and not win.parent.closed:
            win.desktop_id = -1
            self.popup_stack.insert(0, win)
            self.popup_has_focus = True
            win.proxy.set_tiled(EDGE_NONE)
            self._position_popup_on_parent(win)
            logger.info("Placed popup %s (parent=%s)", win.app_id, win.parent.app_id)
            return

        if self.floating_active and self.floating_has_focus:
            # Place in floating overlay
            win.desktop_id = 0
            self.floating_stack.insert(0, win)
            self.floating_has_focus = True
            # Center on output
            win.pos_x = output.x + output.width // 4
            win.pos_y = output.y + output.height // 4
            logger.info("Placed window in floating overlay")
        else:
            desktop = self.current_desktop
            win.desktop_id = self.current_desktop_id
            desktop.add_window(win)
            logger.info("Placed window on desktop %d", self.current_desktop_id)

        # Propose initial dimensions only for floating windows; tiled windows
        # get their dimensions from _apply_manage_state.
        if win.desktop_id == 0:
            self._propose_window_dimensions(win)

    def _propose_window_dimensions(self, win: WindowState):
        """Propose dimensions for a window based on its context."""
        output = self.primary_output
        if output is None:
            return

        if win.desktop_id == 0:
            # Floating: half output size
            w = output.width // 2
            h = output.height // 2
            win.proxy.set_tiled(EDGE_NONE)
            win.proxy.propose_dimensions(w, h)
            logger.info("Proposed floating dimensions %dx%d for %s", w, h, win.app_id)
            return

        desktop = self.desktops[win.desktop_id]

        if desktop.layout == LayoutMode.FULLSCREEN:
            # Fullscreen: compositor handles dimensions
            win.proxy.propose_dimensions(output.width, output.height)
            logger.info("Proposed fullscreen dimensions %dx%d for %s", output.width, output.height, win.app_id)
        elif desktop.layout == LayoutMode.MAX:
            win.proxy.propose_dimensions(output.width, output.height)
            logger.info("Proposed max dimensions %dx%d for %s", output.width, output.height, win.app_id)
        elif desktop.layout == LayoutMode.SPLIT:
            ua_x, ua_y, ua_w, ua_h = self._usable_area(output)
            win.proxy.propose_dimensions(ua_w // 2, ua_h)
            logger.info("Proposed split dimensions %dx%d for %s", ua_w // 2, ua_h, win.app_id)

    def _position_popup_on_parent(self, win: WindowState):
        """Center a popup window over its parent window."""
        output = self.primary_output
        if output is None:
            return
        ua_x, ua_y, ua_w, ua_h = self._usable_area(output)
        parent = win.parent

        # Determine parent center
        if parent is not None and not parent.closed:
            if parent.desktop_id == -1 or parent.desktop_id == 0:
                # Floating or popup parent — use its position + dimensions
                px = parent.pos_x + parent.width // 2
                py = parent.pos_y + parent.height // 2
            elif parent.desktop_id > 0:
                # Tiled parent — compute from usable area + layout
                desktop = self.desktops.get(parent.desktop_id)
                if desktop and desktop.layout == LayoutMode.SPLIT:
                    half_w = ua_w // 2
                    if parent.side == Side.LEFT:
                        px = ua_x + half_w // 2
                    else:
                        px = ua_x + half_w + half_w // 2
                    py = ua_y + ua_h // 2
                else:
                    # MAX/FULLSCREEN — center of usable area
                    px = ua_x + ua_w // 2
                    py = ua_y + ua_h // 2
            else:
                px = ua_x + ua_w // 2
                py = ua_y + ua_h // 2
        else:
            # No valid parent — center on output
            px = ua_x + ua_w // 2
            py = ua_y + ua_h // 2

        # Use win dimensions if known, otherwise estimate as quarter of output
        w = win.width if win.width > 0 else ua_w // 4
        h = win.height if win.height > 0 else ua_h // 4
        win.pos_x = max(ua_x, min(px - w // 2, ua_x + ua_w - w))
        win.pos_y = max(ua_y, min(py - h // 2, ua_y + ua_h - h))

    def _promote_to_popup(self, win: WindowState):
        """Promote a tiled/floating window to the popup layer."""
        self._remove_window(win)
        win.desktop_id = -1
        self.popup_stack.insert(0, win)
        self.popup_has_focus = True
        self._position_popup_on_parent(win)
        self.needs_layout = True
        self.wm_proxy.manage_dirty()

    def _remove_window(self, win: WindowState):
        """Remove a window from all tracking structures."""
        if win.desktop_id == -1:
            if win in self.popup_stack:
                self.popup_stack.remove(win)
            if not self.popup_stack:
                self.popup_has_focus = False
                # Restore desktop focus to the popup's parent window so that
                # a spurious compositor window_interaction event (fired before
                # the closed event) doesn't leave focused_index on the wrong
                # window.
                if win.parent and not win.parent.closed:
                    parent = win.parent
                    if parent.desktop_id == self.current_desktop_id:
                        desktop = self.desktops.get(parent.desktop_id)
                        if desktop:
                            if desktop.layout == LayoutMode.SPLIT:
                                if parent in desktop.left_stack:
                                    desktop.focused_side = Side.LEFT
                                elif parent in desktop.right_stack:
                                    desktop.focused_side = Side.RIGHT
                            elif parent in desktop.windows:
                                desktop.focused_index = desktop.windows.index(parent)
        elif win.desktop_id == 0:
            if win in self.floating_stack:
                self.floating_stack.remove(win)
            if not self.floating_stack:
                self.floating_has_focus = False
        else:
            desktop = self.desktops.get(win.desktop_id)
            if desktop:
                desktop.remove_window(win)

    # -------------------------------------------------------------------
    # Focus management
    # -------------------------------------------------------------------
    def _focus_window(self, seat: SeatState, win: WindowState):
        """Set focus to a specific window."""
        if win is None or win.closed:
            return

        if win.desktop_id == -1:
            # Focusing a popup — bring to top of popup_stack
            if win in self.popup_stack:
                self.popup_stack.remove(win)
                self.popup_stack.insert(0, win)
            self.popup_has_focus = True
            if self.in_manage:
                seat.proxy.focus_window(win.proxy)
            return

        # Focusing a non-popup window — popups stay visible but lose focus
        self.popup_has_focus = False

        # If the window is on a different desktop, switch to it
        if win.desktop_id != 0 and win.desktop_id != self.current_desktop_id:
            self._switch_desktop(win.desktop_id)
        elif win.desktop_id == 0 and not self.floating_active:
            self.floating_active = True

        # Update desktop focus tracking
        if win.desktop_id == 0:
            self.floating_has_focus = True
            if win in self.floating_stack:
                self.floating_stack.remove(win)
                self.floating_stack.insert(0, win)
        else:
            self.floating_has_focus = False
            desktop = self.desktops[win.desktop_id]
            if desktop.layout == LayoutMode.SPLIT:
                if win in desktop.left_stack:
                    desktop.focused_side = Side.LEFT
                    idx = desktop.left_stack.index(win)
                    if idx > 0:
                        desktop.left_stack.remove(win)
                        desktop.left_stack.insert(0, win)
                elif win in desktop.right_stack:
                    desktop.focused_side = Side.RIGHT
                    idx = desktop.right_stack.index(win)
                    if idx > 0:
                        desktop.right_stack.remove(win)
                        desktop.right_stack.insert(0, win)
            else:
                if win in desktop.windows:
                    desktop.focused_index = desktop.windows.index(win)

        if self.in_manage:
            seat.proxy.focus_window(win.proxy)

    def _get_focused_window(self) -> Optional[WindowState]:
        """Get the currently focused window."""
        if self.popup_has_focus and self.popup_stack:
            return self.popup_stack[0]
        if self.floating_active and self.floating_has_focus and self.floating_stack:
            return self.floating_stack[0]
        return self.current_desktop.get_focused_window()

    # -------------------------------------------------------------------
    # Desktop switching
    # -------------------------------------------------------------------
    def _switch_desktop(self, desktop_id: int):
        """Switch to a different desktop."""
        if desktop_id < 1 or desktop_id > 4:
            return
        self.current_desktop_id = desktop_id
        self.needs_layout = True
        logger.info("Switched to desktop %d", desktop_id)

    def _move_window_to_desktop(self, win: WindowState, target_desktop_id: int):
        """Move a window to a different desktop."""
        if win is None or win.closed:
            return
        if target_desktop_id < 0 or target_desktop_id > 4:
            return
        source_desktop_id = win.desktop_id

        # Remove from current location
        self._remove_window(win)

        if target_desktop_id == 0:
            # Move to floating overlay
            win.desktop_id = 0
            output = self.primary_output
            if output:
                win.pos_x = output.x + output.width // 4
                win.pos_y = output.y + output.height // 4
            self.floating_stack.insert(0, win)
            if self.floating_active:
                self.floating_has_focus = True
        else:
            win.desktop_id = target_desktop_id
            target = self.desktops[target_desktop_id]
            if target.layout == LayoutMode.SPLIT:
                target.ensure_window_in_split_stacks(win)
            else:
                target.add_window(win)
            if source_desktop_id == 0:
                self.floating_has_focus = False

        self.needs_layout = True

    # -------------------------------------------------------------------
    # Layout mode switching
    # -------------------------------------------------------------------
    def _set_layout_mode(self, mode: LayoutMode):
        """Switch the current desktop's layout mode."""
        desktop = self.current_desktop
        old_mode = desktop.layout

        if old_mode == mode:
            return

        # Handle fullscreen exit
        if old_mode == LayoutMode.FULLSCREEN:
            for win in desktop.windows:
                if win.is_fullscreen:
                    win.proxy.exit_fullscreen()
                    win.proxy.inform_not_fullscreen()
                    win.is_fullscreen = False

        # Migrate window stacks
        if old_mode == LayoutMode.SPLIT and mode != LayoutMode.SPLIT:
            desktop.migrate_from_split()
        elif old_mode != LayoutMode.SPLIT and mode == LayoutMode.SPLIT:
            desktop.migrate_to_split()

        desktop.layout = mode
        self.needs_layout = True
        # NOTE: Do NOT call propose_dimensions here — it is a manage-sequence-only
        # request.  _apply_manage_state will propose correct dimensions when the
        # next manage sequence runs (triggered by manage_dirty in the caller).

        logger.info("Desktop %d layout changed to %s", desktop.id, mode.value)

    def _is_wine_app(self, win):
        """Check if window is a Wine-based app that shouldn't be force-resized."""
        return win.app_id in ("org.vinegarhq.Sober", "sober", "sober_services")

    # -------------------------------------------------------------------
    # Apply manage state
    # -------------------------------------------------------------------
    def _apply_manage_state(self):
        """Apply window management state during a manage sequence."""
        output = self.primary_output
        if output is None:
            return

        seat = self.seats[0] if self.seats else None

        # Determine the focused window
        focused = self._get_focused_window()

        # Apply fullscreen state for current desktop
        desktop = self.current_desktop
        if desktop.layout == LayoutMode.FULLSCREEN:
            ua_x, ua_y, ua_w, ua_h = self._usable_area(output)
            for win in desktop.windows:
                if not win.closed:
                    if win.width == 0 and win.height == 0:
                        # Window hasn't rendered yet — some clients (Wine/Sober)
                        # won't render at all if fullscreened before their first
                        # frame.  Use propose_dimensions like MAX mode to let
                        # the client render, then fullscreen on a later cycle.
                        if not self._is_wine_app(win):
                            win.proxy.set_tiled(EDGE_ALL)
                        win.proxy.propose_dimensions(ua_w, ua_h)
                        logger.debug("manage_state: FULLSCREEN defer, propose %dx%d for %s (no dims yet)",
                                     ua_w, ua_h, win.app_id)
                    elif win.needs_resize_jolt:
                        # Unstick the window — propose a 1px-off size to force
                        # a new configure, then fullscreen on the next cycle.
                        jolt_w = ua_w - 1
                        win.proxy.propose_dimensions(jolt_w, ua_h)
                        win.needs_resize_jolt = False
                        logger.debug("manage_state: FULLSCREEN JOLT propose %dx%d for %s (cur %dx%d)",
                                     jolt_w, ua_h, win.app_id, win.width, win.height)
                    elif win == focused:
                        if not win.is_fullscreen:
                            win.proxy.fullscreen(output.proxy)
                            win.proxy.inform_fullscreen()
                            win.is_fullscreen = True
                    else:
                        if win.is_fullscreen:
                            win.proxy.exit_fullscreen()
                            win.proxy.inform_not_fullscreen()
                            win.is_fullscreen = False
        else:
            # Ensure no windows are fullscreen in non-fullscreen modes
            for win in desktop.all_windows():
                if not win.closed and win.is_fullscreen:
                    win.proxy.exit_fullscreen()
                    win.proxy.inform_not_fullscreen()
                    win.is_fullscreen = False

        # Propose dimensions for visible windows and inform tiled state
        ua_x, ua_y, ua_w, ua_h = self._usable_area(output)
        if desktop.layout == LayoutMode.MAX:
            for win in desktop.windows:
                if not win.closed:
                    # Skip set_tiled for Wine apps — Wine's Wayland driver
                    # crashes on output removal when tiled state is set.
                    if not self._is_wine_app(win):
                        win.proxy.set_tiled(EDGE_ALL)
                    if win.needs_resize_jolt:
                        jolt_w = ua_w - 1
                        win.proxy.propose_dimensions(jolt_w, ua_h)
                        win.needs_resize_jolt = False
                        logger.debug("manage_state: MAX JOLT propose %dx%d for %s (cur %dx%d)",
                                     jolt_w, ua_h, win.app_id, win.width, win.height)
                    else:
                        win.proxy.propose_dimensions(ua_w, ua_h)
                        logger.debug("manage_state: MAX propose %dx%d for %s (cur %dx%d)",
                                     ua_w, ua_h, win.app_id, win.width, win.height)
        elif desktop.layout == LayoutMode.SPLIT:
            half_w = ua_w // 2
            left_visible = self._visible_top(desktop.left_stack)
            right_visible = self._visible_top(desktop.right_stack)
            for side, win in (("L", left_visible), ("R", right_visible)):
                if win is None:
                    continue
                if not self._is_wine_app(win):
                    win.proxy.set_tiled(EDGE_ALL)
                if win.needs_resize_jolt:
                    # The compositor deduplicates identical proposals, so we
                    # must propose a size different from BOTH the window's
                    # current dimensions AND the previous proposal (half_w).
                    # Using half_w+1 forces a new configure event with a
                    # visually imperceptible 1-pixel difference; the next
                    # manage cycle corrects to the exact half_w.
                    jolt_w = half_w + 1
                    win.proxy.propose_dimensions(jolt_w, ua_h)
                    win.needs_resize_jolt = False
                    logger.debug("manage_state: SPLIT-%s JOLT propose %dx%d for %s (cur %dx%d)",
                                 side, jolt_w, ua_h, win.app_id, win.width, win.height)
                else:
                    win.proxy.propose_dimensions(half_w, ua_h)
                    logger.debug("manage_state: SPLIT-%s propose %dx%d for %s (cur %dx%d)",
                                 side, half_w, ua_h, win.app_id, win.width, win.height)

        # Popup windows — let clients use their natural dialog size
        for win in self.popup_stack:
            if not win.closed:
                win.proxy.set_tiled(EDGE_NONE)
                if win.width == 0 and win.height == 0:
                    # Initial state: no dimensions proposal, client picks its size
                    pass

        # Set focus
        if seat and focused and not focused.closed:
            seat.proxy.focus_window(focused.proxy)
        elif seat:
            seat.proxy.clear_focus()

    # -------------------------------------------------------------------
    # Apply render state
    # -------------------------------------------------------------------
    def _apply_render_state(self):
        """Apply rendering state during a render sequence."""
        output = self.primary_output
        if output is None:
            return

        focused = self._get_focused_window()

        # Hard-clear all tiled windows first. The current desktop layout pass
        # will re-show only the windows that should be visible.
        seen = set()
        for desktop in self.desktops.values():
            for win in desktop.windows + desktop.left_stack + desktop.right_stack:
                key = id(win)
                if key in seen:
                    continue
                seen.add(key)
                if not win.closed:
                    win.proxy.hide()

        # Current desktop
        desktop = self.current_desktop
        self._layout_desktop(desktop, output, focused)

        # Floating overlay
        if self.floating_active:
            self._layout_floating(output, focused)
        else:
            for win in self.floating_stack:
                if not win.closed:
                    win.proxy.hide()

        # Popup layer (always rendered on top, independent of floating toggle)
        self._layout_popups(output, focused)

    def _layout_desktop(self, desktop: Desktop, output: OutputState, focused: Optional[WindowState]):
        """Layout windows on the current desktop."""
        if desktop.layout == LayoutMode.SPLIT:
            self._layout_split(desktop, output, focused)
        else:
            self._layout_single(desktop, output, focused, with_borders=desktop.layout != LayoutMode.FULLSCREEN)

    def _layout_single(self, desktop: Desktop, output: OutputState, focused: Optional[WindowState], with_borders: bool):
        """Fullscreen/Max layout: desktop-focused window visible, others hidden."""
        if with_borders:
            ua_x, ua_y, ua_w, ua_h = self._usable_area(output)
        desktop_focused = desktop.get_focused_window()
        for win in desktop.windows:
            if win.closed:
                continue
            if win == desktop_focused:
                win.proxy.show()
                if win.node:
                    if with_borders:
                        win.node.set_position(ua_x, ua_y)
                    win.node.place_top()
                if with_borders:
                    self._set_borders(win, win == focused)
            else:
                win.proxy.hide()

    def _layout_split(self, desktop: Desktop, output: OutputState, focused: Optional[WindowState]):
        """2-split layout: left and right halves, top of each stack visible."""
        ua_x, ua_y, ua_w, ua_h = self._usable_area(output)
        left_visible = self._visible_top(desktop.left_stack)

        # Left stack — always positioned at left edge
        for i, win in enumerate(desktop.left_stack):
            if win.closed:
                continue
            if i == 0:
                win.proxy.show()
                if win.node:
                    win.node.set_position(ua_x, ua_y)
                self._set_borders(win, win == focused)
            else:
                win.proxy.hide()

        # Right stack — right-aligned to the right edge of usable area
        right_visible = self._visible_top(desktop.right_stack)
        if right_visible and right_visible.width > 0:
            right_x = ua_x + ua_w - right_visible.width
        elif left_visible and left_visible.width > 0:
            right_x = ua_x + left_visible.width
        else:
            right_x = ua_x + ua_w // 2
        for i, win in enumerate(desktop.right_stack):
            if win.closed:
                continue
            if i == 0:
                win.proxy.show()
                if win.node:
                    win.node.set_position(right_x, ua_y)
                self._set_borders(win, win == focused)
            else:
                win.proxy.hide()

        # Z-order: ensure visible windows are on top
        if desktop.left_stack and desktop.left_stack[0].node:
            desktop.left_stack[0].node.place_top()
        if desktop.right_stack and desktop.right_stack[0].node:
            desktop.right_stack[0].node.place_top()

    def _layout_floating(self, output: OutputState, focused: Optional[WindowState]):
        """Layout floating overlay windows."""
        for win in reversed(self.floating_stack):
            if win.closed:
                continue
            win.proxy.show()
            if win.node:
                win.node.set_position(win.pos_x, win.pos_y)
                win.node.place_top()
            self._set_borders(win, win == focused)

    def _layout_popups(self, output: OutputState, focused: Optional[WindowState]):
        """Layout popup/dialog windows (always on top)."""
        for win in reversed(self.popup_stack):
            if win.closed:
                continue
            win.proxy.show()
            if win.node:
                win.node.set_position(win.pos_x, win.pos_y)
                win.node.place_top()
            self._set_borders(win, win == focused)

    @staticmethod
    def _visible_top(stack: list) -> Optional[WindowState]:
        """Return the first non-closed window in a stack, or None."""
        for win in stack:
            if not win.closed:
                return win
        return None

    def _set_borders(self, win: WindowState, is_focused: bool):
        if self.config.border_width <= 0:
            return
        c = _BORDER_FOCUSED if is_focused else _BORDER_UNFOCUSED
        win.proxy.set_borders(EDGE_ALL, self.config.border_width, *c)

    # -------------------------------------------------------------------
    # Interactive move/resize
    # -------------------------------------------------------------------
    def _start_interactive_op(self, seat: SeatState, win: WindowState, mode: str):
        """Start an interactive pointer move or resize operation."""
        if seat.op_active:
            return
        seat.op_active = True
        seat.op_window = win
        seat.op_mode = mode
        seat.op_dx = seat.op_dy = 0
        seat.op_released = False
        if mode == "move":
            win.move_start_x, win.move_start_y = win.pos_x, win.pos_y
        else:
            win.move_start_x, win.move_start_y = win.width, win.height
        seat.proxy.op_start_pointer()

    def _handle_interactive_op(self, seat: SeatState):
        """Handle ongoing interactive operation during manage sequence."""
        win = seat.op_window
        if win is None or win.closed:
            seat.op_active = False
            seat.proxy.op_end()
            return

        if seat.op_mode == "move":
            win.pos_x = win.move_start_x + seat.op_dx
            win.pos_y = win.move_start_y + seat.op_dy
        elif seat.op_mode == "resize":
            new_w = max(100, win.move_start_x + seat.op_dx)
            new_h = max(100, win.move_start_y + seat.op_dy)
            win.proxy.propose_dimensions(new_w, new_h)

        if seat.op_released:
            seat.op_active = False
            seat.proxy.op_end()
            seat.op_window = None

    # -------------------------------------------------------------------
    # Keybinding setup
    # -------------------------------------------------------------------
    def setup_keybindings(self):
        """Register all keybindings with the compositor."""
        if not self.seats:
            logger.warning("No seats available for keybindings")
            return

        seat = self.seats[0]

        # Helper to bind a key
        def bind_key(keysym, modifiers, callback):
            binding = self.xkb_bindings_proxy.get_xkb_binding(seat.proxy, keysym, modifiers)
            def _on_pressed(p, cb=callback, ks=keysym):
                logger.debug("KEY PRESSED: keysym=0x%x", ks)
                cb()
            binding.dispatcher["pressed"] = _on_pressed
            binding.dispatcher["released"] = lambda p: None
            binding.dispatcher["stop_repeat"] = lambda p: None
            self.key_bindings[id(binding)] = (binding, callback)
            self.pending_bindings_to_enable.append(binding)

        def bind_pointer(button, modifiers, press_cb, release_cb=None):
            binding = seat.proxy.get_pointer_binding(button, modifiers)
            binding.dispatcher["pressed"] = lambda p: press_cb()
            binding.dispatcher["released"] = lambda p: (release_cb() if release_cb else None)
            self.pointer_bindings[id(binding)] = (binding, press_cb, release_cb)
            self.pending_bindings_to_enable.append(binding)

